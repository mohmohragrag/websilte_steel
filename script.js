function searchSteel() {
    // تحويل النص إلى حروف كبيرة قبل البحث
    const searchInput = document.getElementById("searchInput").value.trim().toUpperCase();
    const result = document.getElementById("result");
    
    // البحث عن الخامة باستخدام الحروف الكبيرة
    const foundSteel = steelData.find(steel => steel["خامه"].toUpperCase() === searchInput);

    if (foundSteel) {
        // عرض النتيجة باستخدام الحروف الكبيرة
        result.textContent = `الخامة: ${foundSteel["خامه"]}, الصلب: ${foundSteel["صلب"]}`;
    } else {
        result.textContent = "الخامة غير موجودة. يرجى المحاولة مجددًا.";
    }
}
